package com.example.paintapplication;

import android.view.View;

public class FloatPoint {
    float x, y;

    public FloatPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }


}
